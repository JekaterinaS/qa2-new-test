package hw1;

public class Pen {
    private String inkColour;
    private String bodyColour;
    private String material;
    private String manufacturer;
    private double length;

    public String getInkColour() {
        return inkColour;
    }

    public void setInkColour(String inkColour) {
        this.inkColour = inkColour;
    }

    public String getBodyColour() {
        return bodyColour;
    }

    public void setBodyColour(String bodyColour) {
        this.bodyColour = bodyColour;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }
}
